
public class FilesKeyTable {

	String Filename;
	String KeyValue;
	String OriginalString;
	String IP;
	String Port;
	
	public FilesKeyTable(String Filename, String KeyValue, String OriginalString, String IP, String Port){
		this.Filename = Filename;
		this.KeyValue = KeyValue;
		this.OriginalString = OriginalString;
		this.IP = IP;
		this.Port = Port;
	}
}
